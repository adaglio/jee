package com.wps.sneu.core.entities.others;

public class BirthDetail {

	private String countryOfBirth;
	private String districtOfBirth;
	private String placeOfBirth;
	private String registerNumberOfBirth;
	private String zipCodeOfBirth;

	public String getCountryOfBirth() {
		return countryOfBirth;
	}

	public void setCountryOfBirth(String countryOfBirth) {
		this.countryOfBirth = countryOfBirth;
	}

	public String getDistrictOfBirth() {
		return districtOfBirth;
	}

	public void setDistrictOfBirth(String districtOfBirth) {
		this.districtOfBirth = districtOfBirth;
	}

	public String getPlaceOfBirth() {
		return placeOfBirth;
	}

	public void setPlaceOfBirth(String placeOfBirth) {
		this.placeOfBirth = placeOfBirth;
	}

	public String getRegisterNumberOfBirth() {
		return registerNumberOfBirth;
	}

	public void setRegisterNumberOfBirth(String registerNumberOfBirth) {
		this.registerNumberOfBirth = registerNumberOfBirth;
	}

	public String getZipCodeOfBirth() {
		return zipCodeOfBirth;
	}

	public void setZipCodeOfBirth(String zipCodeOfBirth) {
		this.zipCodeOfBirth = zipCodeOfBirth;
	}

}
